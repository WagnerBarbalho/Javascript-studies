function calcularOperacoes () {
    let numero1 = 20
    let numero2 = 10
    let soma = numero1 + numero2;
    let subtracao = numero1 - numero2
    let multiplicação = numero1 * numero2
    let divisao = numero1/ numero2;

    console.log(`a soma de ${numero1} e ${numero2} é: ${soma}`);
    console.log(`a subtração de ${numero1} e ${numero2} é: ${subtracao}`);
    console.log(`a multiplicação de ${numero1} e ${numero2} é: ${multiplicação}`);
    console.log(`a divisao de ${numero1} e ${numero2} é: ${divisao}`)
}
calcularOperacoes();